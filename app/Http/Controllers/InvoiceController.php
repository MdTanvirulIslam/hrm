        }
    }

    public function destroy(Invoice $invoice)
    {
        try {
            $invoice->items()->delete();
            $invoice->terms()->delete();
            $invoice->delete();

            return redirect()->route('invoice.index')
                ->with('success', 'Invoice deleted successfully.');
        } catch (\Exception $e) {
            return back()->with('error', 'Error deleting invoice: ' . $e->getMessage());
        }
    }

    // AJAX method to get sequence number (for create page)
    public function getSequence(Request $request)
    {
        try {
            $companyName = $request->company_prefix;
            $invoiceDate = $request->invoice_date;

            if (!$companyName || !$invoiceDate) {
                return response()->json([
                    'success' => false,
                    'message' => 'Company name and date are required'
                ]);
            }

            // Generate invoice number using the model method
            $invoiceNumber = Invoice::generateInvoiceNumber($companyName, $invoiceDate);

            // Extract sequence from the generated number
            $parts = explode('-', $invoiceNumber);
            $sequence = end($parts);

            return response()->json([
                'success' => true,
                'sequence' => $sequence,
                'invoice_number' => $invoiceNumber
            ]);
        } catch (\Exception $e) {
            return response()->json([
                'success' => false,
                'message' => $e->getMessage()
            ]);
        }
    }
